// Función para obtener una pregunta aleatoria
export const getRandomQuestion = () => {
  const randomIndex = Math.floor(Math.random() * DRINKING_QUESTIONS.length);
  return DRINKING_QUESTIONS[randomIndex];
};

// Función para obtener múltiples preguntas sin repetir
export const getRandomQuestions = (count = 10) => {
  const shuffled = [...DRINKING_QUESTIONS].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};